import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const frontendRoot = path.join(__dirname, "..");
const outFile = path.join(frontendRoot, "public", "firebase-messaging-sw.js");

const REQUIRED = [
  "VITE_FIREBASE_API_KEY",
  "VITE_FIREBASE_AUTH_DOMAIN",
  "VITE_FIREBASE_PROJECT_ID",
  "VITE_FIREBASE_STORAGE_BUCKET",
  "VITE_FIREBASE_MESSAGING_SENDER_ID",
  "VITE_FIREBASE_APP_ID",
];

function firebaseJsVersionFromLock() {
  try {
    const lockPath = path.join(frontendRoot, "package-lock.json");
    const lock = JSON.parse(fs.readFileSync(lockPath, "utf8"));
    const v = lock.packages?.["node_modules/firebase"]?.version;
    if (v && /^\d+\.\d+\.\d+/.test(String(v))) return String(v);
  } catch {
    // ignore
  }
  return "12.12.0";
}

const FIREBASE_JS_VERSION = firebaseJsVersionFromLock();

function loadDotenvFiles() {
  const candidates = [
    ".env.production.local",
    ".env.staging.local",
    ".env.local",
    ".env.staging",
    ".env.production",
    ".env",
  ];
  for (const name of candidates) {
    const p = path.join(frontendRoot, name);
    if (!fs.existsSync(p)) continue;
    const raw = fs.readFileSync(p, "utf8");
    for (const line of raw.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;
      const eq = trimmed.indexOf("=");
      if (eq <= 0) continue;
      const key = trimmed.slice(0, eq).trim();
      let val = trimmed.slice(eq + 1).trim();
      if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue;
      if (
        (val.startsWith('"') && val.endsWith('"')) ||
        (val.startsWith("'") && val.endsWith("'"))
      ) {
        val = val.slice(1, -1);
      }
      if (process.env[key] === undefined || process.env[key] === "") {
        process.env[key] = val;
      }
    }
  }
}

function env(name) {
  const v = process.env[name];
  if (v != null && String(v).trim() !== "") return String(v).trim();
  return "";
}

loadDotenvFiles();

const missing = REQUIRED.filter((k) => !env(k));
if (missing.length) {
  console.error(
    `Missing required env for firebase SW generation: ${missing.join(", ")}.\n` +
      `Set them in CI env or in frontend/.env (see frontend/.env.example).`
  );
  process.exit(1);
}

const firebaseConfig = {
  apiKey: env("VITE_FIREBASE_API_KEY"),
  authDomain: env("VITE_FIREBASE_AUTH_DOMAIN"),
  projectId: env("VITE_FIREBASE_PROJECT_ID"),
  storageBucket: env("VITE_FIREBASE_STORAGE_BUCKET"),
  messagingSenderId: env("VITE_FIREBASE_MESSAGING_SENDER_ID"),
  appId: env("VITE_FIREBASE_APP_ID"),
};

const sw = `/* eslint-disable no-undef */
/* Generated by scripts/generate-firebase-messaging-sw.mjs — do not edit by hand */
// Firebase Messaging service worker (Web Push).
// Uses compat SDK because importScripts is the simplest for SW in Vite.

try {
  importScripts("https://www.gstatic.com/firebasejs/${FIREBASE_JS_VERSION}/firebase-app-compat.js");
  importScripts("https://www.gstatic.com/firebasejs/${FIREBASE_JS_VERSION}/firebase-messaging-compat.js");

  if (self.firebase && self.firebase.initializeApp) {
    self.firebase.initializeApp(${JSON.stringify(firebaseConfig)});

    // Initialize messaging so FCM can deliver background notifications.
    self.firebase.messaging();
  }
} catch (e) {
  // If CDN import fails in dev, the SW should still register (click handling still works),
  // and foreground handling will still function via the page.
}

// Background messages show a notification automatically if payload.notification is set.
// On click, route via deep link (data.deeplink) if present.
self.addEventListener("notificationclick", (event) => {
  if (event && event.notification && event.notification.close) event.notification.close();
  var data = (event && event.notification && event.notification.data) ? event.notification.data : {};
  var deeplink = (data && data.deeplink) || (data && data.FCM_MSG && data.FCM_MSG.data && data.FCM_MSG.data.deeplink);
  // We always open a safe in-app location and pass deeplink via postMessage.
  // The SPA can interpret deeplinks when you're ready to implement routing for them.
  const url = "/nastavitve?section=notifications";

  event.waitUntil(
    (async () => {
      const allClients = await clients.matchAll({ type: "window", includeUncontrolled: true });
      if (allClients.length) {
        allClients[0].focus();
        allClients[0].postMessage({ type: "push_click", deeplink });
        return;
      }
      await clients.openWindow(url);
    })()
  );
});
`;

fs.writeFileSync(outFile, sw, "utf8");
console.log("Wrote", outFile);
